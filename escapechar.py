txt = "We are the so-called \"Vikings\" from the north."
print(txt) #This is a string with a double quote

txt1 = "We are the so-called \'Vikings\' from the north."
print(txt1) #This is a string with a single quote

txt2 = "We are the so-called \nVikings from the north."
print(txt2) #This is a string with a new line

txt3 = "We are the so-called \tVikings from the north."
print(txt3) #This is a string with a tab

txt4 = "We are the so-called \bVikings from the north."
print(txt4) #This is a string with a backspace

txt5 = "We are the so-called \rVikings from the north."
print(txt5) #This is a string with a carriage return