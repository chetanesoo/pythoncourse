a = "Hello"
b = "World"
c = a + b
print(c) #This is a concatenation, it joins the two strings together

d = a + " " + b
print(d) #This is a concatenation, it joins the two strings together with a space in between

e = a * 3
print(e) #This is a concatenation, it repeats the string three times

f = a + " " + b * 3
print(f) #This is a concatenation, it joins the two strings together with a space in between and repeats the string three times
